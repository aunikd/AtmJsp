package com.model;

/**Pojo class for Login instantiation
 * 
 * @author aunik
 *
 */
public class Login {
	private int rno;
	private String pass;
	
	/**Setters and Getters for Login members
	 * 
	 * @return
	 */
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}	
}
